package calc;

public class XuLi {
	public double cong(double so1, double so2) {
		return so1 + so2;
	}
	
	public double tru(double so1, double so2) {
		return so1 - so2;
	}

	public double chia (double so1, double so2) {
		return so1/so2;
	}
	
	public double  nhan (double so1, double so2) {
		return so1*so2;
	}
}

